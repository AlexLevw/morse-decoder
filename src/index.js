const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    const charNumber = expr.length / 10;
    let result = '';
    let subCode = '';

    for (let i = 0; i < charNumber; i++){
        subCode = expr.slice(i * 10, 10 + i * 10).replace(/11/g, "-").replace(/10/g, ".").replace(/0/g, "");
        if (subCode === '**********') {
            result += ' ';
        } else {
            result += MORSE_TABLE[subCode];
        }
    }

    return result;
}

module.exports = {
    decode
}